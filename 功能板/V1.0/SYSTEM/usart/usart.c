//UART底层初始化，时钟使能，引脚配置，中断配置
//此函数会被HAL_UART_Init()调用
//huart:串口句柄

void HAL_UART_MspInit(UART_HandleTypeDef *huart)
{
    //GPIO端口设置
	GPIO_InitTypeDef GPIO_Initure;
	
	if(huart->Instance==USART1)//如果是串口1，进行串口1 MSP初始化
	{
		__HAL_RCC_GPIOA_CLK_ENABLE();			//使能GPIOA时钟
		__HAL_RCC_USART1_CLK_ENABLE();			//使能USART1时钟
	
		GPIO_Initure.Pin=GPIO_PIN_9;			//PA9
		GPIO_Initure.Mode=GPIO_MODE_AF_PP;		//复用推挽输出
		GPIO_Initure.Pull=GPIO_PULLUP;			//上拉
		GPIO_Initure.Speed=GPIO_SPEED_FREQ_HIGH;//高速
		GPIO_Initure.Alternate=GPIO_AF7_USART1;	//复用为USART1
		HAL_GPIO_Init(GPIOA,&GPIO_Initure);	   	//初始化PA9

		GPIO_Initure.Pin=GPIO_PIN_10;			//PA10
		HAL_GPIO_Init(GPIOA,&GPIO_Initure);	   	//初始化PA10
		
#if EN_USART1_RX
		HAL_NVIC_EnableIRQ(USART1_IRQn);				//使能USART1中断通道
		HAL_NVIC_SetPriority(USART1_IRQn,3,3);			//抢占优先级3，子优先级3
#endif	
	}
	
	if(huart->Instance==USART2)//如果是串口2，进行串口2 MSP初始化
	{
		__HAL_RCC_GPIOA_CLK_ENABLE();			//使能GPIOA时钟
		__HAL_RCC_USART2_CLK_ENABLE();			//使能USART2时钟
	
		GPIO_Initure.Pin=GPIO_PIN_2;			//PA2
		GPIO_Initure.Mode=GPIO_MODE_AF_PP;		//复用推挽输出
		GPIO_Initure.Pull=GPIO_PULLUP;			//上拉
		GPIO_Initure.Speed=GPIO_SPEED_FREQ_HIGH;//高速
		GPIO_Initure.Alternate=GPIO_AF7_USART2;	//复用为USART2
		HAL_GPIO_Init(GPIOA,&GPIO_Initure);	   	//初始化PA2

		GPIO_Initure.Pin=GPIO_PIN_3;			//PA3
		GPIO_Initure.Pull=GPIO_PULLUP;
		HAL_GPIO_Init(GPIOA,&GPIO_Initure);	   	//初始化PA3
		
#if EN_USART2_RX
		HAL_NVIC_EnableIRQ(USART2_IRQn);				//使能USART2中断通道
		HAL_NVIC_SetPriority(USART2_IRQn,3,3);			//抢占优先级3，子优先级3
#endif	
	}

}

void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart)
{
	if(huart->Instance==USART1)//如果是串口1
	{
		if((USART_RX_STA&0x8000)==0)//接收未完成
		{
			if(USART_RX_STA&0x4000)//接收到了0x0d
			{
				if(aRxBuffer[0]!=0x0a)USART_RX_STA=0;//接收错误,重新开始
				else USART_RX_STA|=0x8000;	//接收完成了 
			}
			else //还没收到0X0D
			{	
				if(aRxBuffer[0]==0x0d)USART_RX_STA|=0x4000;
				else
				{
					USART_RX_BUF[USART_RX_STA&0X3FFF]=aRxBuffer[0] ;
					USART_RX_STA++;
					if(USART_RX_STA>(USART_REC_LEN-1))USART_RX_STA=0;//接收数据错误,重新开始接收	  
				}		 
			}
		}
	}
	
	if(huart->Instance==USART2)//如果是串口2
	{
		if((aRxBuffer2[0]==0xbe)&&(USART2_RX_BUF[0]!=0xbe))
		{
			USART2_RX_STA=0;
			USART2_RX_BUF[USART2_RX_STA]=aRxBuffer2[0];
		}
		else if(USART2_RX_BUF[0]==0xbe)
		{
			USART2_RX_STA++;
			if(USART2_RX_STA==1)
			{
				USART2_RX_BUF[USART2_RX_STA]=aRxBuffer2[0];
			  length2=aRxBuffer2[0];
			}
			else
			{USART2_RX_BUF[USART2_RX_STA]=aRxBuffer2[0];}
			if(USART2_RX_STA>length2)USART2_RX_STA=0;//接收数据错误,重新开始接收	 
			if(USART2_RX_STA==length2)modebus_rec_flag2=1;
		}
	}
}

int Usart2_Data_Requst()
{
	if(modebus_rec_flag2)
	{
		if((USART2_RX_BUF[0]==0xbe)&&(USART2_RX_BUF[1]==0x03)&&(USART2_RX_BUF[2]==0xff)&&(USART2_RX_BUF[3]==0xff))
		{
			USART2_RX_BUF[0]=0;
			USART2_RX_BUF[1]=0;
			USART2_RX_BUF[2]=0;
			USART2_RX_BUF[3]=0;
			modebus_rec_flag2=0;
			return 1;
		}
		else
		{
			USART2_RX_BUF[0]=0;
			USART2_RX_BUF[1]=0;
			USART2_RX_BUF[2]=0;
			USART2_RX_BUF[3]=0;
			modebus_rec_flag2=0;
			return 0;
		}
	}
	else
	{
		return 0;
	}
}

float Float2Byte(float floatNum, unsigned char *byteAr)
{
	int i=0;
	char *pchar = (char*)&floatNum;
	for(i=0;i<sizeof(float);i++)
	{
		*byteAr = *pchar;
		pchar++;
		byteAr++;
	}
}

